public class Operadores {
    public static void main(String[] args) throws Exception {

        // OPERADORES: Son simbolos que sirven para hacer operaciones con variables o valores
        // ARITMÉTICOS:

        int a = 5;
        int b = 6;
        int c = a + b; // suma los valores - se puede usar / * y -

        int d = a % 2; // el resto nos puede servir por ejemplo para saber si un numero es par o impar

        // ASIGNACIÓN:
        int e = 5;
        e=10;
        e+=15;
        e-=2;
        e*=4;
        e/=2;


        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
